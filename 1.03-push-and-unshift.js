// Both push and unshift adds elements to the beginning.

// Example of unshift

let twentyThree = "XXIII";
let romanNumerals = ['XXI', 'XXII']

romanNumerals.unshift('XIX', 'XX');
// This will add the value to the beginning

// Example of push

ronamNumerals.push(twentyThree);
//This will add the value at the end.


